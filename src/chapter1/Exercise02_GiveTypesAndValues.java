/**
 * 1.1.2
 * Give the type and value of each of the following expressions.
 */
public class Exercise02_GiveTypesAndValues {
    public static void main(String[] args) {
        System.out.println("Type is double, value is " + (1 + 2.62) / 2);       // 1.81
        System.out.println("Type is double, value is " + (1 + 2 + 3 + 4.0));    // 10.0
        System.out.println("Type is boolean, value is " + (4.1 >= 4));          // true
        System.out.println("Type is string, value is " + (1 + 2 + "3"));        // 33
    }
}
